# 动爻白话解释
MOVING_LINE_BAIHUA = {
    '阳动': '主动变化，外在行动，明显改变',
    '阴动': '渐变，内在变化，潜移默化',
    '化进': '变化向好，越来越好的趋势',
    '化退': '变化向差，越来越差的趋势',
    '化回头生': '变化后得到支持，有助力',
    '化回头克': '变化后受到制约，有阻碍',
}

def get_hexagram_baihua(hexagram_name: str) -> Dict:
    """获取卦象白话解读"""
    return HEXAGRAM_BAIHUA.get(hexagram_name, {
        '含义': '此卦较为复杂，需要结合具体情况分析',
        '像什么': '像生活中的变化和挑战',
        '怎么做': '谨慎行事，随机应变',
        '吉凶': '平',
    })

def get_deity_baihua(deity_type: str, question_type: str = '财运') -> str:
    """获取用神白话解释"""
    deity_info = DEITY_BAIHUA.get(deity_type, {})
    return deity_info.get(question_type, deity_info.get('含义', '代表相关的人或事'))

def get_moving_line_baihua(line_yin_yang: str, change_type: str = '') -> str:
    """获取动爻白话解释"""
    base = MOVING_LINE_BAIHUA.get(line_yin_yang, '有变化发生')
    if change_type:
        change = MOVING_LINE_BAIHUA.get(change_type, '')
        return f"{base}，{change}" if change else base
    return base

def generate_full_baihua(hexagram_name: str, deity_type: str, 
                         question_type: str, moving_lines: list) -> Dict:
    """生成完整的白话解读"""
    hexagram_info = get_hexagram_baihua(hexagram_name)
    deity_info = get_deity_baihua(deity_type, question_type)
    
    moving_lines_baihua = []
    for line in moving_lines:
        yin_yang = '阳动' if line.get('阴阳') == '阳' else '阴动'
        baihua = get_moving_line_baihua(yin_yang)
        moving_lines_baihua.append({
            '位置': line.get('位置', 0),
            '白话': baihua,
        })
    
    return {
        '卦象': {
            '卦名': hexagram_name,
            **hexagram_info,
        },
        '用神': {
            '类型': deity_type,
            '白话': deity_info,
        },
        '动爻': moving_lines_baihua,
        '总结': f"此卦{hexagram_info['吉凶']}，{hexagram_info['怎么做']}",
    }
